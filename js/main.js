// Function swap color for logotype at menu

function menuChangeColorOn() {
	document.getElementById('menuLogoText').style.color = '#4b71f0';
	document.getElementById('menuLogo').style.color = '#fff';
}

function menuChangeColorOff() {
	document.getElementById('menuLogoText').style.color = '#fff';
	document.getElementById('menuLogo').style.color = '#4b71f0';
}